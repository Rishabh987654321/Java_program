package ClassesandObjects;

public class StandardLibraryinjava {
    public static void main(String[] args) {

        //Math.sqrt
        System.out.println(Math.sqrt(16));//return 4.0 a float value
        System.out.println(Math.sqrt(24));//returns 4.898979...


        //floor value
        System.out.println(Math.floor(5.8));//5.0
        System.out.println(Math.floor(5.1));//5.0

        //ceil value
        System.out.println(Math.ceil(5.1));//6.0
        System.out.println(Math.ceil(5.8));//6.0
    }
}
