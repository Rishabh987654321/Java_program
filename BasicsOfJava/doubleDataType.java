package BasicsOfJava;

public class doubleDataType {
    public static void main(String[] args) {
        double x = 5;//double stores any real value,it can be decimal or integer both,it gives result in decimal value
        double y = 2;
        double z = x / y;
        System.out.println(z);//result will be 2.5 as both x and y are converted to double then z is printed as it is initialised as double hence the result is 2.5
        double a = 5 / 2;
        System.out.println(a);//result will be 2.0 as it first divide 5/2 which answer is 2 and then converts into double i.e. is 2.0


    }
}
