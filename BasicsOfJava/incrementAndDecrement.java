package BasicsOfJava;

public class incrementAndDecrement {
    public static void main(String[] args) {
        int x = 5;
        char ch = 'a';
        System.out.println(x++);//means print the current value of x and then increment it(5)
        System.out.println(x);//means print the value of x,in this case the incremented value(6)
        System.out.println(++x);//means increment first and then print the value(7)
        System.out.println(++ch);// output will be b

    }


}
